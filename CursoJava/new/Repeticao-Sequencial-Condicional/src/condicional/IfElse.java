package condicional;

public class IfElse {
    public static void main(String[] args) {

        int hora = 10;

        if (hora < 12) {
            System.out.println("Bom dia");
        } else if (hora < 18) {
            System.out.println("Boa tarde");
        } else {
            System.out.println("Boa noite");
        }

    }
}
