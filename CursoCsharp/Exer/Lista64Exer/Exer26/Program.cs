/*
 * 26) Escreva um programa que exiba os números de 1 a 100 na tela em ordem decrescente. 
 */

namespace Exer26
{
    public class Program
    {
        static void Main(string[] args)
        {
            for (int i = 100; i >= 1; i--)
            {
                System.Console.WriteLine(i);
            }
        }
    }
}