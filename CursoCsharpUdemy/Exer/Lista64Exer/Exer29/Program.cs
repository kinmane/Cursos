/*
 * 29) Faça um programa que exiba os números de 1 até 2000. 
 */

namespace Exer29
{
    public class Program
    {
        static void Main(string[] args)
        {
            for (int i = 1; i <= 2000; i++)
            {
                System.Console.WriteLine(i);
            }
        }
    }
}