"""
3- Fazer um programa para ler quatro valores inteiros A, B, C e D. A seguir, 
calcule e mostre a diferença do produto de A e B pelo produto de C e D 
segundo a fórmula: DIFERENCA = (A * B - C * D)
"""

a = int(input("Informe o primeiro valor inteiro: "))
b = int(input("Informe o segundo valor inteiro: "))
c = int(input("Informe o terceiro valor inteiro: "))
d = int(input("Informe o quarto valor inteiro: "))

difference = a + b - c + d

print("DIFERENÇA: ", difference)
